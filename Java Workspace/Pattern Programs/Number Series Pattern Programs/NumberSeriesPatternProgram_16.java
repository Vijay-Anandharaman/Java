package in.workspace.main;

public class NumberSeriesPatternProgram_16
{
    public static void main(String args[])
    {
        for(int i=7;i>=1;i--)
        {
            for(int j=i;j>=1;j--)
            {
                System.out.print(j + " ");
            }
            System.out.println();
        }
    }
}
